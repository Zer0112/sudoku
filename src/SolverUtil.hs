fsudoku :: [SudokuField] -> Maybe (V.Vector (V.Vector Bit))
fsudoku sud = sequence $ V.fromList $ allEntries sud

-- converted SudokuField to for Solver
-- >>> take 5 $ allEntries initField
-- [Just [0,0,0,0,0,0,0,0,0],Just [0,0,0,0,0,0,0,0,0],Just [0,0,0,0,0,0,0,0,0],Just [0,0,0,0,0,0,0,0,0],Just [0,0,0,0,0,0,0,0,0]]
allEntries :: [SudokuField] -> [Maybe (V.Vector Bit)]
allEntries sud = map (\x -> lookup x mappingDigitVector) digList
  where
    entryDigitList :: [SudokuField] -> [Digit]
    entryDigitList = map (\(SudokuField _ _ d) -> d)
    digList = entryDigitList sud


-- >>> lookup One mappingDigitVector
-- Just [1,0,0,0,0,0,0,0,0]
mappingDigitVector :: [(Digit, V.Vector Bit)]
